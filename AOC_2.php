	'A' => 'Rock',
	'B' => 'Paper',
	'C' => 'Scissors',
	'X' => 'Rock',
	'Y' => 'Paper',
	'Z' => 'Scissors'
];

$moveScoreMap = [
	'Rock' => 1,
	'Paper' => 2,
	'Scissors' => 3
];

$outcomeScoreMap = [
	'Win' => 6,
	'Lose' => 0,
	'Draw' => 3
];

$totalScore = 0;
$counter = 0;
$oppMove = '';
$myMove = '';
foreach ($inputMovesArray as $index => $move) {
	$counter++;
	if ($counter === 4) {
		$counter = 0;
		continue;
	}

	if ($move === ' ') {
		continue;
	}

	if ($counter === 1) {
		$oppMove = $moveNameMap[$move];
		continue;
	} else {
		$myMove = $moveNameMap[$move];
		$totalScore += $moveScoreMap[$myMove];
	}

	if ($oppMove === $myMove) {
		$totalScore += $outcomeScoreMap['Draw'];
		continue;
	}

	if ($myMove === 'Rock') {
		if ($oppMove === 'Paper') {
			$totalScore += $outcomeScoreMap['Lose'];
		}

		if ($oppMove === 'Scissors') {
			$totalScore += $outcomeScoreMap['Win'];
		}
	}

	if ($myMove === 'Paper') {
		if ($oppMove === 'Scissors') {
			$totalScore += $outcomeScoreMap['Lose'];
		}

		if ($oppMove === 'Rock') {
			$totalScore += $outcomeScoreMap['Win'];
		}
	}

	if ($myMove === 'Scissors') {
		if ($oppMove === 'Rock') {
			$totalScore += $outcomeScoreMap['Lose'];
		}

		if ($oppMove === 'Paper') {
			$totalScore += $outcomeScoreMap['Win'];
		}
	}
